def get_bright_range(color):
    return ["darkest " + color, "darker " + color, "dark " + color,
            color, "light " + color, "lighter " + color, "lightest " + color]


def name_from_color(c):
    # a = (c >> 32) & 0xFF
    r = (c >> 16) & 0xFF
    g = (c >> 8) & 0xFF
    b = c & 0xFF
    return '#%02x%02x%02x' % (r, g, b)

